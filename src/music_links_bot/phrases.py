def pick_phrase(key: str, seed: str | None = None) -> str:
    phrases = PHRASES[key]
    if seed is None:
        return phrases[0]

    digest = hashlib.sha256(seed.encode("utf-8")).hexdigest()
    index = int(digest, 16) % len(phrases)
    return phrases[index]
